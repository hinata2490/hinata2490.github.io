const avg7prev = () => weightAvg(-13, -7);

/* ==================== 食事ロジック ==================== */
function dinnerIndex(dk) {
  if (state.dinnerPlan[dk] != null) return state.dinnerPlan[dk];
  // 日付から決まるローテーション(記録がなくても毎日変わる)
  const [y, m, d] = dk.split('-').map(Number);
  const dayNum = Math.floor(Date.UTC(y, m - 1, d) / 86400000);
  return dayNum % RECIPES.length;
}
function todayRecipe() { return RECIPES[dinnerIndex(todayKey())]; }

function dinnerTotals(r) {
  const soupExtra = r.soup ? { kcal: 0, protein: 0, price: 0 } : { kcal: SOUP.kcal, protein: SOUP.protein, price: SOUP.price };
  return {
    kcal: r.kcal + DINNER_RICE.kcal + soupExtra.kcal,
    protein: r.p + DINNER_RICE.protein + soupExtra.protein,
    price: r.price + soupExtra.price,
  };
}
function dayTotals() {
  const r = todayRecipe();
  const dt = dinnerTotals(r);
  let kcal = dt.kcal, protein = dt.protein, price = dt.price;
  for (const m of FIXED_MEALS) { kcal += m.kcal; protein += m.protein; price += m.price; }
  return { kcal, protein, price };
}
function todayMeals() {
  const tk = todayKey();
  if (!state.meals[tk]) state.meals[tk] = { breakfast: false, protein: false, banana: false, dinner: false };
  return state.meals[tk];
}

// 今日が筋トレ日かどうか(記録済み or 今日やる予定)
function isTrainDay() {
  if (state.workouts[todayKey()]) return true;
  const adv = todayWorkoutAdvice();
  return !!adv.menu;
}

// カロリー目標との比較と、不足時の具体的な足し方
function kcalAdvice(tot) {
  const train = isTrainDay();
  const range = train ? KCAL_TARGET.train : KCAL_TARGET.rest;
  const dayLabel = train ? '筋トレ日' : '休養日';
  let mark = '✅', hint = '';
  if (tot.kcal < range[0]) {
    mark = '⚠️';
    const need = range[0] - tot.kcal;
    const adds = [];
    if (need >= 70) adds.push('夕食のご飯を250→300g(+78kcal)');
    if (need >= 140) adds.push('卵1個か納豆1P(+76〜80kcal)');
    if (need >= 220) adds.push('バナナもう1本(+90kcal)');
    if (need >= 300) adds.push('朝食の卵を2→3個(+76kcal)');
    hint = `<br>→ あと${need}kcal:${adds.join('、')} で埋めるのがおすすめ(少なすぎると筋肉が増えない)`;
  } else if (tot.kcal > range[1] + 150) {
    mark = '⚠️';
    hint = '<br>→ 今日はやや多め。気にするほどではないが、続く場合は夕食を軽めのレシピに';
  }
  return { range, dayLabel, mark, hint };
}

