const STORE_KEY = 'fitnessApp_v1';

function defaultState() {
  return {
    workouts: {},   // date -> { menu, results: {exId: [reps,...]} }
    weights: {},    // date -> kg
    meals: {},      // date -> { breakfast, protein, banana, dinner }
    dinnerPlan: {}, // date -> recipe index
    prog: {},       // exId -> { target, streak }
    chat: [],       // { who: 'user'|'bot', text }
  };
}

let state = loadState();

function loadState() {
  try {
    const raw = localStorage.getItem(STORE_KEY);
    if (!raw) return defaultState();
    return Object.assign(defaultState(), JSON.parse(raw));
  } catch (e) {
    return defaultState();
  }
}
function save() { localStorage.setItem(STORE_KEY, JSON.stringify(state)); }

/* ==================== 日付ユーティリティ ==================== */
function dateKey(d) {
  const y = d.getFullYear(), m = String(d.getMonth() + 1).padStart(2, '0'), dd = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${dd}`;
}
function todayKey() { return dateKey(new Date()); }
function keyOffset(n) { const d = new Date(); d.setDate(d.getDate() + n); return dateKey(d); }
function fmtShort(key) { const [, m, d] = key.split('-'); return `${Number(m)}/${Number(d)}`; }

// 月曜はじまりの今週の日付キー一覧
function thisWeekKeys() {
  const d = new Date();
  const day = (d.getDay() + 6) % 7; // 月=0
  const keys = [];
  for (let i = 0; i < 7; i++) {
    const dd = new Date(d);
    dd.setDate(d.getDate() - day + i);
    keys.push(dateKey(dd));
  }
  return keys;
}

/* ==================== 筋トレロジック ==================== */
// 進行状態: state.prog[exId] = { lvl, target, sets }
// 旧形式({target, streak}のみ)はLv1・2セットとして読む
function exProg(ex) {
  const p = state.prog[ex.id] || {};
  const lvl = Math.min(p.lvl || 0, ex.levels.length - 1);
  const lv = ex.levels[lvl];
  return {
    lvl,
    lv,
    target: p.target != null ? Math.min(p.target, lv.cap) : lv.base,
    sets: p.sets || ex.sets,
  };
}
function exTarget(ex) { return exProg(ex).target; }
function targetLabel(ex) {
  const p = exProg(ex);
  const side = p.lv.perSide ? '左右各' : '';
  return `${side}${p.target}${ex.unit} × ${p.sets}セット`;
}
function exName(ex) {
  const p = exProg(ex);
  return p.lvl > 0 ? `${p.lv.name}(Lv${p.lvl + 1})` : p.lv.name;
}

// 直近の筋トレ記録(今日を除く)
function lastWorkout(excludeToday) {
  const keys = Object.keys(state.workouts).sort().reverse();
  for (const k of keys) {
    if (excludeToday && k === todayKey()) continue;
    return { date: k, ...state.workouts[k] };
  }
  return null;
}
function weeklyWorkoutCount() {
  return thisWeekKeys().filter(k => state.workouts[k]).length;
}

// 今日おすすめのメニュー(前回と逆)
function suggestedMenu() {
  const last = lastWorkout(true);
  if (!last) return 'A';
  return last.menu === 'A' ? 'B' : 'A';
}

// 今日の筋トレ提案テキスト
function todayWorkoutAdvice() {
  const tk = todayKey();
  if (state.workouts[tk]) {
    return { title: `筋トレ完了!(メニュー${state.workouts[tk].menu})`, sub: 'おつかれさま。今日はしっかり食べてプロテインも忘れずに。', done: true };
  }
  const count = weeklyWorkoutCount();
  const last = lastWorkout(true);
  if (last && last.date === keyOffset(-1)) {
    return { title: '今日は休養日 🛌', sub: '昨日筋トレしたので今日は休み。筋肉は休んでいる間に育つ。散歩くらいはOK。', rest: true };
  }
  if (count >= 3) {
    return { title: '今週のノルマ達成済み 🎉', sub: `今週すでに${count}回実施。今日は休んでOK。やりたければ軽めの足踏みだけでも。`, rest: true };
  }
  const menu = suggestedMenu();
  return { title: `今日は筋トレ日:メニュー${menu}`, sub: `週${count}/3回。15〜20分、終了時に「あと3回できそう」くらいで切り上げる。`, menu };
}

// 進行判定:全セットで目標達成するたびに1段階進む(1回の記録で1段階まで)
//   ① 回数 +step(レベル内の上限まで)
//   ② 上限に達したら 2→3セット
//   ③ 3セットでも達成したら次のレベルの種目へ(回数・セットはリセット)
// 達成できなかった日は現状維持(下がりはしない)。フォームが崩れる場合は手動で軽くしてOK。
function applyProgression(menu, results) {
  const notes = [];
  for (const ex of WORKOUTS[menu]) {
    const sets = results[ex.id];
    if (!sets || sets.every(v => v === 0)) continue;
    const p = exProg(ex);
    const done = sets.filter(v => v > 0);
    const hit = done.length >= p.sets && done.every(v => v >= p.target);
    if (!hit) continue;
    const step = ex.step || 1;
    let next = { lvl: p.lvl, target: p.target, sets: p.sets };
    if (p.target < p.lv.cap) {
      next.target = Math.min(p.target + step, p.lv.cap);
      notes.push(`${p.lv.name}:次回から ${next.target}${ex.unit} にアップ(フォームが崩れたら戻してOK)`);
    } else if (p.sets < 3) {
      next.sets = 3;
      notes.push(`${p.lv.name}:上限到達!次回から 3セット に挑戦`);
    } else if (p.lvl < ex.levels.length - 1) {
      const nl = ex.levels[p.lvl + 1];
      next = { lvl: p.lvl + 1, target: nl.base, sets: ex.sets };
      notes.push(`🎉 レベルアップ!「${p.lv.name}」を卒業 → 次回から「${nl.name}」(${nl.base}${ex.unit}×${ex.sets}セット)`);
    } else {
      notes.push(`${p.lv.name}:最終レベルを完全制覇!ここまで来たらチャットで次の相談を`);
    }
    state.prog[ex.id] = next;
  }
  return notes;
}

/* ==================== 体重ロジック ==================== */
function weightAvg(fromOffset, toOffset) {
  const vals = [];
  for (let i = fromOffset; i <= toOffset; i++) {
    const v = state.weights[keyOffset(i)];
    if (v) vals.push(v);
  }
  if (!vals.length) return null;
  return vals.reduce((a, b) => a + b, 0) / vals.length;
}
