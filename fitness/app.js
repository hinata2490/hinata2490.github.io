function renderWeight() {
  const a = avg7(), prev = avg7prev();
  $('ws-avg').innerHTML = a ? `${a.toFixed(1)}<small>kg</small>` : '--';
  if (a && prev) {
    const diff = a - prev;
    const sign = diff > 0 ? '+' : '';
    $('ws-diff').innerHTML = `${sign}${diff.toFixed(1)}<small>kg</small>`;
    $('ws-diff').style.color = diff <= 0 ? 'var(--green)' : 'var(--orange)';
  } else {
    $('ws-diff').innerHTML = '--';
  }
  const w = state.weights[todayKey()];
  if (w) $('weight-input').value = w;

  // SVG チャート(直近30日)
  const pts = [];
  for (let i = -29; i <= 0; i++) {
    const v = state.weights[keyOffset(i)];
    if (v) pts.push({ i: i + 29, v });
  }
  const svg = $('weight-chart');
  if (pts.length < 2) {
    svg.innerHTML = '<text x="160" y="75" fill="#8d8371" font-size="12" text-anchor="middle">2日分以上記録するとグラフが出ます</text>';
  } else {
    const vals = pts.map(p => p.v);
    const min = Math.min(...vals) - 0.3, max = Math.max(...vals) + 0.3;
    const X = i => 10 + (i / 29) * 300;
    const Y = v => 128 - ((v - min) / (max - min)) * 116;
    const line = pts.map(p => `${X(p.i).toFixed(1)},${Y(p.v).toFixed(1)}`).join(' ');
    const dots = pts.map(p => `<circle cx="${X(p.i).toFixed(1)}" cy="${Y(p.v).toFixed(1)}" r="2.5" fill="#44598c"/>`).join('');
    // 目標レンジ 60.5〜61.5 の帯
    let band = '';
    if (min < 61.5 && max > 60.5) {
      const y1 = Y(Math.min(61.5, max)), y2 = Y(Math.max(60.5, min));
      band = `<rect x="10" y="${y1.toFixed(1)}" width="300" height="${(y2 - y1).toFixed(1)}" fill="rgba(113,140,96,0.16)"/>`;
    }
    svg.innerHTML = `${band}
      <polyline points="${line}" fill="none" stroke="#44598c" stroke-width="2"/>${dots}
      <text x="12" y="14" fill="#8d8371" font-size="10">${max.toFixed(1)}kg</text>
      <text x="12" y="136" fill="#8d8371" font-size="10">${min.toFixed(1)}kg</text>`;
  }

  // 履歴(直近14件)
  const keys = Object.keys(state.weights).sort().reverse().slice(0, 14);
  $('weight-history').innerHTML = keys.length
    ? keys.map(k => `<div class="wh-row"><span class="wh-date">${k}</span><b>${state.weights[k].toFixed(1)} kg</b></div>`).join('')
    : '<div class="muted">まだ記録がありません。朝起きてトイレの後に測ると安定します。</div>';
}

